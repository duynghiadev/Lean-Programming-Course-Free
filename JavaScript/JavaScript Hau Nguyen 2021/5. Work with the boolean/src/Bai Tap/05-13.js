// Addition of logging "Say hello" in multiple languages

function sayHelloInDifferentLanguages() {
  console.log('Say hello'); // English
  console.log('Xin chào'); // Vietnamese
  console.log('こんにちは'); // Japanese
  console.log('Hallo'); // Dutch -> tiếng Hà Lan
}
sayHelloInDifferentLanguages();
