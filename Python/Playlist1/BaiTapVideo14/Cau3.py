# Trình bày ý nghĩa toán tử /, //, %, **, and, or, is

"""
1. / (chia thường): chia 2 số và trả về kết quả dưới dạng số thực

5 / 2 (kết quả: 2.5)
"""

"""
2. // (chia lấy phần nguyên): chia 2 số và trả về kết quả phần nguyên

5 / 2 (kết quả 2)
"""

"""
3. % (chia lấy phần dư): chia 2 số và trả về phần dư

5 % 2 (kết quả 1)
"""

"""
4. ** (lũy thừa): tích lũy thừa của 1 số

2 ** 3 = 2^3 (kết quả 8)
"""

"""
5. and (và): toán tử logic "và"

True and False (kết quả: False)
"""

"""
6. or (hoặc): toán tử logic "hoặc"

True or False (kết quả True)
"""

